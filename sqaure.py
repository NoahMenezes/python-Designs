import turtle
turtle.bgcolor('#0d0d2b')
noah=turtle.Turtle()



noah.pencolor('cyan')
    
noah.begin_fill()

for i in range(3):
    noah.forward(100)
    noah.left(90)
noah.forward(100)

noah.penup()
noah.forward(150)
noah.pendown()

for i in range(3):
    noah.forward(100)
    noah.left(90)
noah.forward(100)

noah.penup()
noah.forward(150)
noah.pendown()

for i in range(3):
    noah.forward(100)
    noah.left(90)
noah.forward(100)

noah.penup()
noah.forward(150)
noah.pendown()

for i in range(3):
    noah.forward(100)
    noah.left(90)
noah.forward(100)

noah.forward(150)
noah.right(90)
noah.forward(150)
noah.right(90)
noah.forward(150)
noah.right(90)
noah.forward(150)


noah.end_fill()
turtle.done()
